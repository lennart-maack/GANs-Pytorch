    """
    Checks the length of the BUS dataset and prints it
    """
    n_classes = 0
    n_folds_per_class = 0
    patients = 0
    for i, item in enumerate(BUS_set):
        n_classes += 1
        for j, fold in enumerate(item):
            n_folds_per_class += 1
            for m, patient in enumerate(fold):
                patients +=1 

    print ("n_classes: ", n_classes)
    print ("n_folds_per_class: ", n_folds_per_class)
    print("patients: ", patients)


